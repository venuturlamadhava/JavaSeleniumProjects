package Assignments;

public class CountNumberOfLetter {

	public static void main(String[] args) {
		String S = "qwesdrfghjkmlwesrdfghjknm";
        int count = 0;

        for(int i=0;i<S.length();i++)
        {
        	S.charAt(i);
        	count++;
        }
        System.out.println(count);
		
	}

}
